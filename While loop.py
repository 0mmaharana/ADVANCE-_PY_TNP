#ex1 while with else
i=1
print("\nThe no.s are")
while(i<=10):
    print(i)
    i=i+1
else:
    print("while loop ended")    

#ex1 while with break and else
j=1
print("\nThe no.s are(using break):")
while(j<=10):
    print(j)
    j=j+1
    if(j==3):
        break;
else:
    print("while loop ended")    
