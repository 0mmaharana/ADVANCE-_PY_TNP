#2.Write a program to find the area and perimeter of a triangle.
base=float(input("enter length of the base of a triangle :"))
height=float(input("enter height of a triangle :"))
A=1/2*base*height
print("area of triangle :", A)
a=float(input("enter 1st side of the triangle :"))
b=float(input("enter 2nd side of the triangle :"))
c=float(input("enter 3rd side of the triangle :"))
P=a+b+c
print("perimeter of triangle :", P)
